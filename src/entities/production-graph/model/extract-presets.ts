import type { ExtractPresetId } from './types';
import { productionLayers } from './production-layers';

export interface ExtractPreset {
  id: ExtractPresetId;
  label: string;
  prompt: string;
}

export const defaultExtractPrompt = `Проанализируй изображение как senior art director и prompt engineer для AI image production.

Сделай максимально подробный production-ready prompt, который позволит сгенерировать новое изображение максимально близко к референсу по визуальной структуре, продуктовой логике, композиции, свету, стилю и типографике.

Пиши как полноценное техническое задание для генерации изображения. Не сокращай описание до общих слов. Разделяй наблюдения по слоям и сохраняй важные детали.

Верни результат строго в такой структуре:

[SUBJECT / PRODUCT]
Главный объект, материал, форма, состояние, детали поверхности, важные признаки, что должно остаться резким и узнаваемым.

[ACTION / OBJECT STATE]
Положение объекта, жест, состояние, динамика, наклон, направление, что происходит в кадре.

[COMPOSITION]
Ориентация кадра, крупность, размещение объектов, передний/средний/задний план, визуальная иерархия, negative space, дополнительные элементы, глубина сцены.

[CAMERA]
Ракурс, план, перспектива, ощущение объектива, дистанция, глубина резкости, фокус, дисторсия или отсутствие дисторсии.

[BACKGROUND / ENVIRONMENT]
Фон, среда, окружение, поверхность или отсутствие поверхности, атмосферные детали, что должно быть чистым или не отвлекать.

[LIGHT]
Источники света, направление, мягкость/жесткость, контраст, тени, блики, rim light, ambient occlusion, что именно свет подчеркивает.

[STYLE]
Визуальный язык, техника, реализм, рекламная эстетика, уровень детализации, тип production/look, степень премиальности.

[COLOR / GRADE]
Палитра, контраст, насыщенность, температура, цветокор, тональность, допустимые и недопустимые акценты.

[TYPOGRAPHY / TEXT / CALLOUTS]
Все видимые надписи, язык, точное написание, расположение, стиль шрифта, стрелки, callouts, UI/инфографика, если они есть. Если текста нет, напиши, что текста нет.

[NEGATIVE CONSTRAINTS]
Что нельзя добавлять или ломать: логотипы, водяные знаки, лишний текст, грязь, деформации, лишние люди, неправильные материалы, плохую типографику и т.д.

Если изображение рекламное или продуктовая обложка, обязательно опиши его как commercial poster / product advertising layout. Если изображение не рекламное, адаптируй формулировки к его жанру, но структуру сохрани.

Пиши на том языке, который лучше подходит для дальнейшей генерации: технические секции и ключевые production-термины можно оставлять на английском, конкретные русские надписи с изображения сохраняй без перевода.`;

export const extractPresets: ExtractPreset[] = [
  {
    id: 'default',
    label: 'Default',
    prompt: defaultExtractPrompt,
  },
  ...productionLayers.map((layer) => ({
    id: layer.id,
    label: layer.label,
    prompt: layer.prompt,
  })),
];

export function getExtractPreset(id?: ExtractPresetId) {
  return extractPresets.find((preset) => preset.id === id) ?? extractPresets[0];
}
